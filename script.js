        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                const href = this.getAttribute('href');
                if (!href || href === '#') return;

                e.preventDefault();
                const target = document.querySelector(href);
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });

        function setupBackToTop() {
            const button = document.querySelector('.back-to-top');
            if (!button) return;

            const updateVisibility = () => {
                button.classList.toggle('is-visible', window.scrollY > 620);
            };

            button.addEventListener('click', () => {
                window.scrollTo({
                    top: 0,
                    behavior: reducedMotionQuery.matches ? 'auto' : 'smooth'
                });
            });

            window.addEventListener('scroll', updateVisibility, { passive: true });
            updateVisibility();
        }

        // Initialize
        setupHeroSlideshow();
        renderPrograms(programs);
        setupScrollReveal();
        setupBackToTop();

        if (mobileProgramsQuery.addEventListener) {
            mobileProgramsQuery.addEventListener('change', handleProgramsViewportChange);
        } else {
            mobileProgramsQuery.addListener(handleProgramsViewportChange);
        }

        // Close modal on escape key
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape') {
                closeModal();
            }
        });
    
